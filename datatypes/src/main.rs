fn main() {
    //Datatypes
    //Scalar Types : Integer, floating point , Boolean, Character type
    //Integer
    let x = 5;

    //Floating point
    let y = 2.55;

    //Boolean
    let a = true;

    let b: bool = false;

    //Character Type
    let c = 'Z';

    println!("Value of integer x is x: {},value of floating point y is y: {}, boolean a and b is {}, {} and character c is : {}  ", x, y, a, b, c)
}
